        // Reemplazo robusto: traduce data-i18n, data-i18n-key y clases i18n-*
        function applyLang(lang) {
            const dict = t[lang] || t.es;
            // expone idioma y diccionario para otros scripts (previas, reCAPTCHA, etc.)
            window.mceCurrentLang = lang;
            window.mceTranslations = dict;
            if (document && document.documentElement) {
                document.documentElement.lang = lang;
                document.documentElement.setAttribute('data-site-lang', lang);
            }
            // Atributo data-i18n
            document.querySelectorAll('[data-i18n]').forEach(el => {
                const key = el.dataset.i18n;
                if (dict[key]) {
                    const span = el.querySelector('span');
                    if (span) span.textContent = dict[key];
                    else el.textContent = dict[key];
                }
            });
            // Placeholders especรญficos
            document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
                const key = el.dataset.i18nPlaceholder;
                if (dict[key]) el.placeholder = dict[key];
            });
            // Atributo data-i18n-key (marcado desde mapClass)
            document.querySelectorAll('[data-i18n-key]').forEach(el => {
                const key = el.dataset.i18nKey;
                if (dict[key]) el.innerHTML = dict[key];
            });
            // Clases i18n-*
            document.querySelectorAll('[class*=\"i18n-\"]').forEach(el => {
                if (el.dataset.i18n) return; // si ya tiene data-i18n no sobrescribimos (para no borrar รญconos)
                const key = Array.from(el.classList).find(c => c.startsWith('i18n-'))?.replace('i18n-','');
                if (key && dict[key]) el.innerHTML = dict[key];
            });
            localStorage.setItem('siteLang', lang);
            if (typeof window !== 'undefined' && window.dispatchEvent) {
                window.dispatchEvent(new CustomEvent('mce-lang-changed', { detail: { lang } }));
            }
        }

        initSiteLang();
    })();
